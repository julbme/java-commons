package me.julb.commons.constants;

import lombok.AccessLevel;
import lombok.NoArgsConstructor;

/**
 * A class to list constants in Chars format. <br>
 * @author Julb.
 */
@NoArgsConstructor(access = AccessLevel.PRIVATE)
public class Chars {

    /**
     * The '0' character.
     */
    public static final char ZERO = '0';

    /**
     * The '9' character.
     */
    public static final char NINE = '9';

    /**
     * The 'A' character.
     */
    public static final char A_UPPERCASE = 'A';

    /**
     * The 'Z' character.
     */
    public static final char Z_UPPERCASE = 'Z';

    /**
     * The 'a' character.
     */
    public static final char A_LOWERCASE = 'a';

    /**
     * The 'f' character.
     */
    public static final char F_LOWERCASE = 'f';

    /**
     * The 's' character.
     */
    public static final char S_LOWERCASE = 's';

    /**
     * The 'v' character.
     */
    public static final char V_LOWERCASE = 'v';

    /**
     * The 'z' character.
     */
    public static final char Z_LOWERCASE = 'z';

    /**
     * The "/" character.
     */
    public static final char SLASH = '/';

    /**
     * The "-" character.
     */
    public static final char DASH = '-';

    /**
     * The "." character.
     */
    public static final char DOT = '.';

    /**
     * The "," character.
     */
    public static final char COMMA = ',';

    /**
     * The "*" character.
     */
    public static final char STAR = '*';

    /**
     * The ":" character.
     */
    public static final char SEMICOLON = ':';

    /**
     * The "|" character.
     */
    public static final char PIPE = '|';

    /**
     * The "_" character.
     */
    public static final char UNDERSCORE = '_';

    /**
     * The " " character.
     */
    public static final char SPACE = ' ';

    /**
     * The "=" character.
     */
    public static final char EQUAL = '=';

    /**
     * The "&amp;" character.
     */
    public static final char AND = '&';

    /**
     * The """ character.
     */
    public static final char QUOTATION_MARK = '"';

    /**
     * The "?" character.
     */
    public static final char QUESTION_MARK = '?';

    /**
     * The "!" character.
     */
    public static final char EXCLAMATION_MARK = '!';

    /**
     * The "^" character.
     */
    public static final char CIRCUMFLEX = '^';

    /**
     * The "$" character.
     */
    public static final char DOLLAR = '$';

    /**
     * The "{" character.
     */
    public static final char LEFT_CURLY_BRACKET = '{';

    /**
     * The "}" character.
     */
    public static final char RIGHT_CURLY_BRACKET = '}';

    /**
     * The "&lt;" character.
     */
    public static final char LEFT_ANGLE_BRACKET = '<';

    /**
     * The "&gt;" character.
     */
    public static final char RIGHT_ANGLE_BRACKET = '>';

    /**
     * The "@" character.
     */
    public static final char AT = '@';
}
